#include <bits/stdc++.h>
#define ll long long
#define nl endl
#define pii pair<int, int>
#define MOD = 1e9;
using namespace std;
void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int min_candies = *min_element(a.begin(), a.end());
    long long total_candies_eaten = 0;

    for (int i = 0; i < n; i++)
    {
        total_candies_eaten += a[i] - min_candies;
    }

    cout << total_candies_eaten << endl;
}

signed main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
}
