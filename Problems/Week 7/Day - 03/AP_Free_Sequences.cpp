bool check(vector<int> seq, int n)
{
    for (int i = 0; i < n - 2; i++)
    {
        for (int j = i + 1; j < n - 1; j++)
        {
            for (int k = j + 1; k < n; k++)
            {
                if (seq[j] - seq[i] == seq[k] - seq[j])
                    return false;
            }
        }
    }

    return true;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> arr(n);

    for (int i = 0; i < n; i++)
        cin >> arr[i];

    if (check(arr, n))
        yes;

    else
        no;
}
